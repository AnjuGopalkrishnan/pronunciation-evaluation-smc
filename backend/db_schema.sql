-- Database: mispronunciation

-- DROP DATABASE IF EXISTS mispronunciation;

CREATE DATABASE mispronunciation
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LC_COLLATE = 'English_India.1252'
    LC_CTYPE = 'English_India.1252'
    LOCALE_PROVIDER = 'libc'
    TABLESPACE = pg_default
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

-- Table: public.user_progress

-- DROP TABLE IF EXISTS public.user_progress;

CREATE TABLE IF NOT EXISTS user_progress
(
    username character varying COLLATE pg_catalog."default" NOT NULL,
    word character varying COLLATE pg_catalog."default" NOT NULL,
    done boolean,
    favorite boolean,
    score integer,
    CONSTRAINT user_progress_pkey PRIMARY KEY (username, word)
)

-- Table: public.users

-- DROP TABLE IF EXISTS public.users;

CREATE TABLE IF NOT EXISTS public.users
(
    user_id integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    username character varying COLLATE pg_catalog."default" NOT NULL,
    password character varying COLLATE pg_catalog."default" NOT NULL,
    email character varying COLLATE pg_catalog."default",
    CONSTRAINT users_pkey PRIMARY KEY (user_id),
    CONSTRAINT email_unique UNIQUE (email)
)

-- Table: public.words

-- DROP TABLE IF EXISTS public.words;

CREATE TABLE IF NOT EXISTS public.words
(
    wordid integer NOT NULL GENERATED BY DEFAULT AS IDENTITY ( INCREMENT 1 START 1 MINVALUE 1 MAXVALUE 2147483647 CACHE 1 ),
    word character varying COLLATE pg_catalog."default",
    weekid integer NOT NULL,
    CONSTRAINT wordid_pk PRIMARY KEY (wordid)
)

